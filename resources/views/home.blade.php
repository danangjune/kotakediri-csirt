                <p>Dalam rangka mengantispasi meningkatnya jumlah insiden serangan keamanan informasi di Kota Kediri, maka dibutuhkan adanya Tim Tanggap Insiden Siber (KEDIRIKOTA-CSIRT) untuk menghadapi ancaman serta penanganannya.</p>
