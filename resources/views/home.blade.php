                <p>Computer Security Incident Response Team (CSIRT) adalah tim yang menyediakan pelayanan dalam mencegah, menanggulangi dan menanggapi insiden keamanan siber, pada suatu wilayah (constituency) yang bertanggung jawab atas penerimaan, pemantauan dan penanganan laporan dan aktivitas insiden keamanan siber. Tim CSIRT akan bertanggung jawab penuh untuk memonitor dan mengelola berbagai isu-isu terkait dengan keamanan internet untuk menjaga aset informasi dan komunikasi dari seluruh unit-unit aktifitas organisasi.</p>
